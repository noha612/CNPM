    public void actionPerformed(ActionEvent e){
        JButton btnClicked = (JButton)e.getSource();
        if(btnClicked.equals(btnQuayLai)){
            btnQuayLaiDuocNhan();
            return;
        }
        for(int i = 0; i < ltk.size(); i++){
            if(btnClicked.equals(listChiTiet.get(i))){
                btnChiTietDuocNhan(i);
                return;
            }
        }
    }
    
