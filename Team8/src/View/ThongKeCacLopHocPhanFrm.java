    class ResultTableModel extends DefaultTableModel{
        private String[] columnNames = {"STT", "Lớp học phần", "Giảng viên", "Sỹ số", "Điểm trung bình", "Tỷ lệ qua môn(%)", "Chi tiết"};
        private final Class<?>[] columnTypes = new Class<?>[] {Integer.class, String.class,String.class, Integer.class, Float.class, Integer.class, JButton.class};
        @Override public int getColumnCount() {
            return 7;
        }
 
        @Override public int getRowCount() {
            return ltk.size();
        }
 
        @Override public String getColumnName(int columnIndex) {
            return columnNames[columnIndex];
        }
 
        @Override public Class<?> getColumnClass(int columnIndex) {
            return columnTypes[columnIndex];
        }
        
        @Override public Object getValueAt(final int rowIndex, final int columnIndex) {
            switch (columnIndex) {
                case 0: 
                    return rowIndex+1;
                case 1: 
                    return ltk.get(rowIndex).getLopHocPhan();
                case 2: 
                    return ltk.get(rowIndex).getGiangVien();
                case 3: 
                    return ltk.get(rowIndex).getSySo();
                case 4: 
                    return ltk.get(rowIndex).getDiemTB();
                case 5: 
                    return ltk.get(rowIndex).getTyLeQuaMon();
                case 6: 
                    return listChiTiet.get(rowIndex);
                default: return "Error";
            }
        } 
    }
